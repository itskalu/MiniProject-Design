package model;


public class LP
{
    String barcode;
    String title;
    String artist;
    String publicationDate;
    String description;
    
    public LP(String barcode, String title, String artist, String publicationDate, String description)
    {
        this.barcode = barcode;
        this.title = title;
        this.artist = artist;
        this.publicationDate = publicationDate;
        this.description = description;
       
    }

   
}
